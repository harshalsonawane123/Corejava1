package com.edubridgestatickeywordwithvariable;

public class StaticVariable {
	//static keyword with variable name
	/* when any variable name is non static  and we are trying to 
	 * access that variable inside the main method*/
	static float percentage=96.30f;
	
	public static void main(String[] args) {
		System.out.println(percentage);

	}

}
